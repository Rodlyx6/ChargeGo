/**
 * 充电类型对照表
 * 
 * 前端发送请求时，chargeType 必须是以下值之一：
 * 
 * ┌─────────────┬──────────────┬────────────┬──────────────┐
 * │ chargeType  │ 描述         │ 单价       │ 示例（2小时）│
 * ├─────────────┼──────────────┼────────────┼──────────────┤
 * │ 1           │ 快充         │ 10元/小时  │ 20元         │
 * │ 2           │ 普通充电     │ 5元/小时   │ 10元         │
 * │ 3           │ 慢充         │ 2元/小时   │ 4元          │
 * └─────────────┴──────────────┴────────────┴──────────────┘
 * 
 * ============================================================
 * 创建订单请求示例
 * ============================================================
 * 
 * POST /order/create
 * Content-Type: application/json
 * Authorization: Bearer <token>
 * 
 * {
 *   "stationId": "101",
 *   "chargeType": 1,      // 快充
 *   "chargeTime": 2       // 2小时
 * }
 * 
 * ============================================================
 * 创建订单响应示例
 * ============================================================
 * 
 * {
 *   "code": 200,
 *   "msg": "预约成功",
 *   "data": "1710000000ABCD"  // 订单号
 * }
 * 
 * ============================================================
 * 查询订单详情请求示例
 * ============================================================
 * 
 * GET /order/1710000000ABCD
 * Authorization: Bearer <token>
 * 
 * ============================================================
 * 查询订单详情响应示例（待支付状态）
 * ============================================================
 * 
 * {
 *   "code": 200,
 *   "msg": "查询成功",
 *   "data": {
 *     "orderNo": "1710000000ABCD",
 *     "userId": "1",
 *     "stationId": "101",
 *     "status": 0,
 *     "statusDesc": "待支付",
 *     "chargeType": 1,
 *     "chargeTypeDesc": "快充",
 *     "chargeTime": 2,
 *     "expectedAmount": 20.0,
 *     "actualAmount": null,
 *     "refundAmount": null,
 *     "actualChargeTime": null,
 *     "payTime": null,
 *     "createTime": "2026-03-16T10:00:00",
 *     "updateTime": "2026-03-16T10:00:00"
 *   }
 * }
 * 
 * ============================================================
 * 查询订单详情响应示例（已完成状态）
 * ============================================================
 * 
 * {
 *   "code": 200,
 *   "msg": "查询成功",
 *   "data": {
 *     "orderNo": "1710000000ABCD",
 *     "userId": "1",
 *     "stationId": "101",
 *     "status": 2,
 *     "statusDesc": "已完成",
 *     "chargeType": 1,
 *     "chargeTypeDesc": "快充",
 *     "chargeTime": 2,
 *     "expectedAmount": 20.0,
 *     "actualAmount": 7.5,      // 实际充电45分钟 = 0.75小时 × 10元/小时
 *     "refundAmount": 12.5,      // 20 - 7.5 = 12.5
 *     "actualChargeTime": 0.75,  // 实际充电0.75小时
 *     "payTime": "2026-03-16T10:05:00",
 *     "createTime": "2026-03-16T10:00:00",
 *     "updateTime": "2026-03-16T10:50:00"
 *   }
 * }
 * 
 * ============================================================
 * 订单状态对照表
 * ============================================================
 * 
 * status = 0  → "待支付"   (用户已预约，等待支付)
 * status = 1  → "充电中"   (用户已支付，正在充电)
 * status = 2  → "已完成"   (充电已结束，可查看退款)
 * status = 3  → "已取消"   (订单已取消)
 * 
 * ============================================================
 * 前端回显逻辑
 * ============================================================
 * 
 * 1. 创建订单后，获取订单号
 * 2. 根据订单号查询订单详情
 * 3. 使用 chargeTypeDesc 显示充电类型（不要自己转换 chargeType）
 * 4. 显示 expectedAmount 作为预期金额
 * 5. 支付后，定时查询订单详情
 * 6. 订单完成后，显示 actualAmount 和 refundAmount
 * 
 */
